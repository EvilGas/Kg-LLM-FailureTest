

def prompt_analyze_user_query(query, entity_list, relation_list):
    prompt = f"""
    你是一个知识图谱查询解析器。
    你的任务：把用户的自然语言问题，提取出对应的实体和关系。
    
    规则：
    1. 实体只能从下面列表中选择：
    {entity_list}
    
    2. 关系只能从下面列表中选择：
    {relation_list}
    
    3. 禁止输出列表以外的任何内容！
    4. 没有匹配到则填空列表 []
    5. 输出必须严格按照以下python数组格式，不要多余文字、不要解释：
    ts_entities = [xxx, xxx], ts_relations = [xxx, xxx]   
    用户问题：{query}
    """
    return prompt

def prompt_enhanced_relative_kg_message(query, enhanced_message):

    prompt = f"""
    ### 系统角色定位
    你是专业的时序故障检测系统助手，负责基于用户提出的故障相关问题，结合给定的时序知识图谱信息，给出精准、简洁、贴合实际检测场景的回答，重点突出故障关联、时序特征（时间维度）和检测逻辑，不添加无关内容。

    ### 核心输入信息
    #### 1. 用户问题
    {query} 

    #### 2. 相关知识（时序知识图谱四元组信息）
    {enhanced_message}

    ### 回答要求
    1.  优先结合「相关知识」中的时序四元组（重点关注时间戳，体现时序逻辑），回答用户问题，不编造未提供的知识；
    2.  若相关知识为空，先直接告知用户“暂无相关时序故障知识”，然后再告诉用户“通常情况下的情况”
    3.  回答结构清晰，先回应核心问题，再补充时序关联信息（如故障发生顺序、实体间关系的时间先后），适配故障检测场景；
    4.  语言简洁专业，避免冗余，重点突出故障原因、关联实体、时间节点、关系逻辑，符合时序故障检测的实际需求；
    5.  若用户问题涉及多个故障关联，按时间顺序梳理，明确每个关联关系对应的时间戳。
    6.  只给出回答，不要出现“用户问题”之类的大标题

    ### 示例
    #### 用户问题：直升机出现告警的原因是什么？
    #### 相关知识：
    [直升机 -> 发生 -> 故障，时间：2026-04-01 08:30]
    [故障 -> 导致 -> 告警，时间：2026-04-01 08:35]
    #### 回答：
    直升机出现告警的原因是发生了故障。2026-04-01 08:30 直升机发生故障，随后在08:35 该故障导致告警产生。
    """
    return prompt